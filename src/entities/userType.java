package entities;

/**
 *
 * @author Marek
 */
public class userType {
    int typeId;
    String typeName; 

    public int getTypeId() {
        return typeId;
    }

    public void setTypeId(int typeId) {
        this.typeId = typeId;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }
    
    public userType(int id, String name) {
        typeId = id;
        typeName = name;
    }
}

